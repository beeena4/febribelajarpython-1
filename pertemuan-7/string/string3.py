# contoh string 3, string3.py
a = "Belajar" 
b = "Python" 

# Operator Concatenation
concatenation = a + b  # Hasil: 'BelajarPython'

# Operator Perulangan
repeated_a = a * 2  # Hasil: 'BelajarBelajar'

# Operator Slice
slice_character = a[1]  # Hasil: 'e'
range_slice = a[1:4]  # Hasil: 'ela'

# Operator in
in_a = 'a' in a  # Hasil: True
not_in_z = 'z' not in a  # Hasil: True

# Operator Raw String
raw_string = r'print r\'n prints \n dan print R\'\nprints \n'

# Operator Format
formatted_string = "Hasil: {}, {}".format(a, b)  # Hasil: 'Hasil: Belajar, Python'

# Menampilkan hasil
print("a : " + a)
print("b : " + b)
print ("-------------------------------")
print("Concatenation:", concatenation)
print("Repeated a:", repeated_a)
print("Slice character a[1]:", slice_character)
print("Range slice a[1:4]:", range_slice)
print("'a' in a:", in_a)
print("'z' not in a:", not_in_z)
print("Raw String:", raw_string)
print("Formatted String:", formatted_string)

