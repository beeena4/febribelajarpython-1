# Mendefinisikan variabel string
nama = 'FTIK USM'  # Variabel "nama" menyimpan string 'FTIK USM'
pesan = "Belajar bahasa Python di FTIK USM"  # Variabel "pesan" menyimpan string panjang

# Mengakses karakter pertama dari string "nama"
print ("Nama [0]: ", nama[0])  # Mengambil karakter pertama (indeks 0) dari "nama", yaitu 'F'

# Mengakses substring dari indeks 1 sampai 3 (karakter indeks 4 tidak termasuk)
print ("Pesan [1:4]: ", pesan[1:4])  # Mengambil karakter dari indeks 1 hingga 3 dari "pesan"
# Indeks 1 = 'e', indeks 2 = 'l', indeks 3 = 'a' → hasilnya "ela"

