#Cara mengakses nilai di dalam list Python 
list1 = ['TI', 'SI', 'ILKOM', 2010, 2006]  # List berisi campuran string dan integer
list2 = ["x", "y", "z", "w"]  # List berisi karakter string

# Mencetak elemen pertama dari list1
print("list1 (0): ", list1[0])  # Mengakses elemen pertama (indeks 0) dari list1, yaitu 'TI'
# Mencetak elemen dari indeks 1 hingga 4 dari list2
print("list2 [1:5]: ", list2[1:5])  # Mengakses elemen indeks 1 hingga 4 (elemen terakhir tidak termasuk), yaitu ['y', 'z', 'w']

